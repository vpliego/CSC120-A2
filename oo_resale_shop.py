class ResaleShop:
    #here we are defining what the inventory is so that the main has a place to store the computers. 
    def __init__(self, inventory):
        self.inventory = inventory 
      
      #this should allow computers to be added into the inventory whenever one is bought from another seller.
    def add_to_inventory():
        inventory = inventory + 1
  
    # What attributes will it need?
    # How will you set up your constructor?
